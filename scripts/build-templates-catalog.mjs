function slugify(title) {
  return title.toLowerCase()
    .replace(/['’.]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function main() {
  const templates = [];
  const seen = new Set();
  for (const [category, entries] of Object.entries(CATALOG)) {
    for (const [title, subtitle, sensitivity] of entries) {
      const slug = slugify(title);
      const key = slug;
      // Disambiguate duplicate slugs across categories (e.g. Subcontractor, Sponsorship).
      let finalSlug = slug;
      if (seen.has(key)) {
        finalSlug = `${slug}-${category.split('-')[0]}`;
      }
      seen.add(finalSlug);
      templates.push({ slug: finalSlug, title, category, subtitle, sensitivity });
    }
  }
  const out = {
    _meta: {
      purpose: 'Master catalog of every contract/document template CyberSygn authors and owns.',
      bar: 'Each template authored to the gold standard in templates-content/master-services-agreement.md. Constitution 1.13 + 1.14.',
      framing: 'Every template ships as an excellent, customizable starting draft with attorney-review framing. Not legal advice.',
      total: templates.length,
    },
    templates,
  };
  return out;
}

const out = main();
await writeFile(OUT, JSON.stringify(out, null, 2));
console.log(`Wrote ${out.templates.length} catalog entries to ${OUT}`);
const byCat = {};
for (const t of out.templates) byCat[t.category] = (byCat[t.category] || 0) + 1;
for (const [c, n] of Object.entries(byCat)) console.log(`  ${c}: ${n}`);
const high = out.templates.filter(t => t.sensitivity === 'high').length;
console.log(`High-sensitivity: ${high} | Normal: ${out.templates.length - high}`);
